    <br>
    <br>
    <p>PARA alumnos y profesores QUIEN necesite calcular host mínimo o máximo, brodcast, submascara, clasificación de red y hacer cálculos con números binarios (LA). </p> 
    <p>"IP MASTER" es un auxiliar escolar QUE ayuda a determinar especificaciones de una red y hacer operaciones binarias A DIFERENCIA DE otras calculadoras que solo realizan alguna de estas especificaciones a la vez NUESTRO PRODUCTO integra estas calculadoras para auxiliar a nuestro usuario.</p>
